    <h2 class="rules">1. Speluitleg</h2>
    <p>We gaan een jaar lang op zoek naar de groenste eenheid binnen FOS Open Scouting!</p>
    <p>Als eenheid verzamel je zoveel mogelijk punten door uitdagingen aan te gaan. Vanaf 1 oktober tot 30 april worden elke maand een vijftal uitdagingen gelanceerd. Elke eenheid kan de uitdagingen uitvoeren en het bewijsmateriaal (foto/video) via deze site inzenden.</p>
    <p>Er zijn mooie en nuttige prijzen te winnen voor de eenheden met de hoogste punten. Ook de eenheid met de meest creatieve inzendingen valt in de prijzen, doe dus zot in de interpretatie van opdrachten en met je video-skills!</p>
</div>
<div class="regels">
    <h2 class="rules">2. Faire scores</h2>
     <p>Met elke uitdaging valt een hoeveelheid punten te winnen. Per uitdaging kan elke eenheid welgeteld één inzending doen. Werk samen met een andere eenheid en verhoog je punten voor die uitdaging. Maar let op, want met elke eenheid kan je slechts 1 keer samenwerken!</p>
     <p>Elke inzending passeert het alziend oog van onze jury. De jury is volledig onpartijdig en bestaat uit vertegenwoordigers van meerdere eenheden. Wanneer een inzending afgekeurd zou worden, kan je als eenheid een nieuwe poging doen. Na goedkeuring van een inzending zal ze op deze site gepubliceerd worden en zijn de punten binnen.</p>
</div>
<div class="regels">
    <h2 class="rules">3. Spelregels</h2>
    <ul>
     <li class="rulesItem">We zijn trotse scouts. Leden op ingezonden foto's & video’s dragen voorbeeldig hun uniform + das. Een eenheidsvlag is altijd welkom!</li>
     <li class="rulesItem">Op alle inzendingen zijn leden van FOS Open Scouting te zien.</li>
     <li class="rulesItem">Samenwerken mag, tegenwerken niet. Andere eenheden saboteren is niet toegestaan.</li>
     <li class="rulesItem">Een uitdaging mag niet uitgevoerd worden met enkel leid(st)ers, wees creatief met je hele tak of eenheid.</li>
     <li class="rulesItem">Geen vandalisme.</li>
     <li class="rulesItem">Safety first! Maar discipline, dedication and friendship zijn natuurlijk ook belangrijk.</li>
     <li class="rulesItem">De scoutswet is heilig, we leven deze altijd met hart en ziel na!</li>

    </ul>
</div>
